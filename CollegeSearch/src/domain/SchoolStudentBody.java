package domain;

public class SchoolStudentBody {

	private int numberOfStudents, foriegnPercent, asianPercent;

	public void setNumberOfStudents(int numberOfStudents) {
		this.numberOfStudents = numberOfStudents;
	}

	public int getNumberOfStudents() {
		return numberOfStudents;
	}

	public void setForiegnPercent(int foriegnPercent) {
		this.foriegnPercent = foriegnPercent;
	}

	public int getForiegnPercent() {
		return foriegnPercent;
	}

	public void setAsianPercent(int asianPercent) {
		this.asianPercent = asianPercent;
	}

	public int getAsianPercent() {
		return asianPercent;
	}
}
